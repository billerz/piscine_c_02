#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		if (str[index] < ' ' || str[index] > '~')
		{
			return (0);
		}
		index++;
	}
	return (1);
}
